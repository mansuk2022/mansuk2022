    public void SortData() {
        for (int i = 0; i < listData.size() - 1; i++) {
            int minPos = i;
            for (int j = i + 1; j < listData.size(); j++) {
                if (listData.get(j).getprice() < listData.get(minPos).getprice()) {
                    minPos = j;
                }

            }
            Collections.swap(listData, minPos, i);
        }
    }

    public static int binarySearch(ArrayList<Tabledata> listData, int low, int high, int value) {
        if (low <= high) {
            int mid = (low + high) / 2;
            if (listData.get(mid).getprice() == value) {
                return mid;
            } else if (listData.get(mid).getprice() > value) {
                return binarySearch(listData, low, mid - 1, value);
            } else {
                return binarySearch(listData, mid + 1, high, value);
            }
        } else {
            return -1;
        }

    }

