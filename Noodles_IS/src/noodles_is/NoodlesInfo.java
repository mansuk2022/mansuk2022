    public void detailsTxt() {
        File file = new File("details.txt");
        try {
            if (!file.isFile()) {
                file.createNewFile();
            }
            FileWriter r = new FileWriter(file);
            for (Tabledata a : listData) {
                String value[] = {a.getbnumber() + "", a.getname() + "", a.getprice() + "", a.getcompany() + "", a.gettype() + "", a.getmdatey() + "//" + a.getmdatem() + "//" + a.getmdated() + "", a.getedatey() + "//" + a.getedatem() + "//" + a.getedated() + ""};
                for (int i = 0; i < value.length; i++) {
                    r.append(value[i]);
                    if (i == value.length - 1) {
                        break;
                    }
                    r.append(",");
                }
                r.append("\n");
            }
            r.flush();
            r.close();
        } catch (IOException e) {
        }
    }
